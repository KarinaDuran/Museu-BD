    private void EnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EnviarActionPerformed

        DefaultTableModel tableModel = (DefaultTableModel) TabelaColecoes.getModel();
        try {

            PreparedStatement stmt;

            if (Mes.isSelected()) {
                query = "SELECT c.NomeColecao, c.Quantidade, y.data FROM (SELECT NomeColecao, COUNT(*) as Quantidade, MONTH(DataEmprestimo) AS data FROM EMPRESTADOS E GROUP BY NomeColecao, MONTH(DataEmprestimo)) c INNER JOIN (SELECT MAX(x.Quantidade) AS maximo, x.data FROM (SELECT COUNT(*) as Quantidade, MONTH(DataEmprestimo) AS data FROM EMPRESTADOS GROUP BY NomeColecao, MONTH(DataEmprestimo)) x GROUP BY x.data) y WHERE c.Quantidade = y.maximo AND c.data = y.data ";

            } else if (Ano.isSelected()) {
                query = "SELECT c.NomeColecao, c.Quantidade, y.data FROM (SELECT NomeColecao, COUNT(*) as Quantidade, YEAR(DataEmprestimo) AS data FROM EMPRESTADOS E GROUP BY NomeColecao, YEAR(DataEmprestimo)) c INNER JOIN (SELECT MAX(x.Quantidade) AS maximo, x.data FROM (SELECT COUNT(*) as Quantidade, YEAR(DataEmprestimo) AS data FROM EMPRESTADOS GROUP BY NomeColecao, YEAR(DataEmprestimo)) x GROUP BY x.data) y WHERE c.Quantidade = y.maximo AND c.data = y.data ";

            }

            stmt = this.connection.prepareStatement(query);
            ResultSet rs = stmt.executeQuery();

            tableModel.setRowCount(0);
            while (rs.next()) {
                ArrayList<Object> resultados = new ArrayList<Object>();
                resultados.add(rs.getString("NomeColecao"));
                resultados.add(String.valueOf(rs.getString("Quantidade")));
                resultados.add(String.valueOf(rs.getString("data")));

                Object[] aux = resultados.toArray();

                tableModel.addRow(aux);

            }

            query = "Select * FROM OBJETO_ARTE o ";
            rs.close();
            stmt.close();
        } catch (SQLException e) {
            throw new RuntimeException(e);
        }
    }//GEN-LAST:event_EnviarActionPerformed

