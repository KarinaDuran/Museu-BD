    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String query = "SELECT COUNT(IdObj) AS quantidade_compras, " ;
        
        if(jRadioButton1.isSelected()) query+= " MONTH(DataAquisicao) AS Data FROM PERMANENTES GROUP BY MONTH(DataAquisicao)";
        else if(jRadioButton4.isSelected())query += " YEAR(DataAquisicao) AS Data FROM PERMANENTES GROUP BY YEAR(DataAquisicao);";
       
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        try {

            PreparedStatement stmt;

            stmt = this.connection.prepareStatement(query);
            ResultSet rs = stmt.executeQuery();

            tableModel.setRowCount(0);
            while (rs.next()) {
                ArrayList <Object> resultados = new ArrayList<Object>();
                resultados.add(String.valueOf(rs.getInt("quantidade_emprestimos")));
                resultados.add(rs.getString("Data"));
      
                Object [] aux = resultados.toArray();

                tableModel.addRow(aux);

            }

   
            rs.close();
            stmt.close();
        }catch (SQLException e) {
            throw new RuntimeException(e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

