    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String query = "SELECT COUNT(IdObj) AS quantidade_emprestimos, " ;
        
        if(jRadioButton1.isSelected()) query+= " MONTH(DataEmprestimo) AS Data FROM EMPRESTADOS GROUP BY MONTH(DataEmprestimo)";
        else if(jRadioButton2.isSelected())query += " YEAR(DataEmprestimo) AS Data FROM EMPRESTADOS GROUP BY YEAR(DataEmprestimo);";
       
  
        try {

           PreparedStatement stmt = this.connection.prepareStatement(query);
            ResultSet rs = stmt.executeQuery();
      DefaultCategoryDataset ds = new DefaultCategoryDataset();
       
    
            while (rs.next()) {
                Number x =rs.getInt("quantidade_emprestimos");
                ds.addValue(x,WIDTH, rs.getString("Data") );
            }
            rs.close();
            stmt.close();
               
            
            JFreeChart lineChart = ChartFactory.createLineChart("Emprestimos por agrupamento", "Data","Quantidade",  ds, PlotOrientation.VERTICAL, true,true,false);
        
            ChartPanel chartPanel = new ChartPanel( lineChart );
 
              chartPanel.setBounds(80, 80, 600, 450);
              
              setContentPane(chartPanel);
         
             
                
              
            }catch (SQLException e) {
             throw new RuntimeException(e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

